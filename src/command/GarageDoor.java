package command;

public class GarageDoor {
	public void up(){
		System.out.println("GarageDoor Up!!");
	}
	
	public void down(){
		System.out.println("GarageDoor Down..");
	}
	
	public void stop(){
		System.out.println("GarageDoor STOP - -");
	}
	
	public void lightOn(){
		System.out.println("GarageDoor's lights on :)");
	}
	
	public void lightOff(){
		System.out.println("GarageDoor's lights off :(");
	}
}
